package com.fellow.domain.enums;

public enum HttpStatusEnum {
    FAILED(100, "失败"),
    SUCCESS(200, "成功"),
    EXCEPTION(300, "异常"),
    ;
    private int status;
    private String desc;

    HttpStatusEnum(int status, String desc) {
        this.status = status;
        this.desc = desc;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
